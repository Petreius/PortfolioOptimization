
public class ActionsMutation {

	public int i = 0;
	public int j = 1;
	public int k = 2;
	
	public ActionsMutation(int i, int j, int k){
		this.i = i;
		this.j = j;
		this.k = k;
	}
	
	public void setI (int i){
		this.i = i;
	}
	public void setJ (int i){
		this.j = i;
	}
	public void setK (int i){
		this.k = i;
	}
	public int getI(){
		return this.i;
	}
	public int getJ(){
		return this.j;
	}
	public int getK(){
		return this.k;
	}
}
